package model;

public class FruitTransaction {
    private final Operation operation;
    private final String fruit;
    private final int quantity;

    public FruitTransaction(Operation operation, String fruit, int quantity) {
        this.operation = operation;
        this.fruit = fruit;
        this.quantity = quantity;
    }

    public Operation getOperation() {
        return operation;
    }

    public String getFruit() {
        return fruit;
    }

    public int getQuantity() {
        return quantity;
    }

    public enum Operation {
        BALANCE("b"),
        SUPPLY("s"),
        PURCHASE("p"),
        RETURN("r");

        private String code;

        Operation(String code) {
            this.code = code;
        }

        public String getCode() {
            return code;
        }
    }

    public static FruitTransaction.Operation valueOfLabel(String label) {
        for (FruitTransaction.Operation e : FruitTransaction.Operation.values()) {
            if (e.getCode().equals(label)) {
                return e;
            }
        }
        throw new RuntimeException("Invalid strategy: " + label);
    }
}
